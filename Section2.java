//Section-2:Write a java program to find simple interest.
class Section2
{
public static void main(String args[])
{
float p,t,r,si;
System.out.println("Principle(p):5000");
p=5000;
System.out.println("Time in years(t):2");
t=2;
System.out.println("Rate of interest(r):5");
r=5;
si=(p*t*r)/100;
System.out.println("Simple interest is:"+si);
}
}